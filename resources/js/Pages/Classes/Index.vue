<template>
  <authenticated-layout>
    <section class="py-5 mt-5">
      <div class="container d-flex gap-4 flex-column justify-content-start align-items-start">
        
        <Link class="btn btn-success" href="/dashboard/classes/create">اضافة فصل</Link>

        <input type="text" class="form-control w-50 mt-2" v-model="search" placeholder="ابحث  ...">

        <div class="table-responsive w-100 bg-white shadow-sm border-top border-success border-2">
          <table class="table align-middle text-center table-bordered mb-0">
            <thead>
              <tr>
                <th>الصف</th>
                <th>اسم الفصل</th>
                <th></th>
              </tr>
            </thead>
            <tbody>
              <tr v-for="cls in classes" :key="cls.id">
                <td>{{ cls.grade.name }}</td>
                <td>{{ cls.name }}</td>
                <td class="d-flex gap-2 justify-content-center">
                  <Link class="btn btn-sm btn-warning" :href="`/dashboard/classes/${cls.id}/edit`">تعديل</Link>
                  <Link class="btn btn-sm btn-danger" @click="destroy(cls.id)" as="button">حذف</Link>
                </td>
              </tr>
              <tr v-if="classes.length == 0">
                <td>لاتوجد فصول</td>
                <td></td>
              </tr>
            </tbody>
          </table>
        </div>
      </div>
    </section>
  </authenticated-layout>
</template>

<script>
import AuthenticatedLayout from '../../Layouts/Authenticated.vue';
import {Link} from '@inertiajs/inertia-vue3'
import debounce from 'lodash/debounce'
export default {
  props: {
    classes: Array,
    filters: Array,
  },
  data() {
    return {
      form: this.$inertia.form({
        name: null,
        school_id: null,
      }),
      search: this.filters.search,
    }
  },
  components: {
    AuthenticatedLayout,
    Link
  },
  watch: {
    search: debounce(function (newSearch, oldSearch) {
      this.$inertia.get('/dashboard/classes', {'search': newSearch}, {preserveState: true, replace: true})
    }, 500)
  },
  methods: {
    filterSchools () {
      this.$inertia.get('/dashboard/classes')
    },
    destroy(id) {
      if (confirm('هل انت متأكد ؟')) {
        this.$inertia.delete(`/dashboard/classes/${id}`)
      }
    }
  }
}
</script>

